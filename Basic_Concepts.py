## printing

# Integers

print (2 + 2)
print(5 + 4 - 3)
print(2*(3+4))
print(10/2)

# Floats

print ((4+8)/2) # Will equal 6.0 
print(1+2+3+6.0+7) # Will equal 19.0

# Exponentiation

print(2**5) # Results in 2^5
print (9 ** (1/2)) # Result will be a float: 3.0

# Quotient

print(20 // 6) # The code above will output 3, because 6 goes into 20 three times. 

# Remainder

print(20 % 6) # % is the Modulo operator or mod  // Result is 2
print(1.25 % 0.5) # Result is 0.25
print(7%(5//2)) # Result is 1: 2 goes 2 times into 5, and 7 % 2 is 1. (7 // 2 -> 2, 4 , 6 = 3    Remainder is 1)



